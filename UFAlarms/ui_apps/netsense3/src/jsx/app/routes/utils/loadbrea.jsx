getCustomers() {
  var that = this;
  $.ajax({
    url: NSN.apiURL + 'customers', 
    data : '',
    xhrFields: {
       withCredentials: true
    },
    method : 'GET',
    dataType : 'json',

    success:function(data){
      var customerList = data;
      var customerID = "";
      console.log(JSON.stringify(data));
      for (var i=0; i<customerList.length; i++) {
          if (customerList[i].name.indexOf("Uber") >= 0) {
            customerID = customerList[i].orgid;
          }
       };
      that.createSite(customerID, that.props.breaMall);
      that.createSite(customerID, that.props.valleyFair);
      },
    error:function(x){
      alert("getCustomers failed");
      }
    });
}

createSite(customerID, site) {
  console.log("Building site: " + JSON.stringify(site));
  var that=this;
  $.ajax({
    url:NSN.apiURL + "customers/" + customerID + "/sites",
    type:"POST",
    data:JSON.stringify(site),
    contentType:"application/json",
    xhrFields: {
      withCredentials: true
    },
    dataType:"json",
    success:function(data){
      var siteID = data.siteid;
      console.log("Site created: " + JSON.stringify(data));
      that.loadNodes(customerID, siteID, (site.name.indexOf("Valley")>=0)?that.props.VFNodes:that.props.BNodes);
//      that.loadNodes();
      },
    error:function(x,y,z){
      alert("createSite failed, possibly because " + site.name + " already exists");
      }
    });
}

